// Operadores Aritiméticos
  
  // Multiplicação *
    console.log('3*5=', 3 * 5)

  // Divisão /
    console.log('10/2=', 10 / 2)

  // Soma +
    console.log('8+5=', 8 + 5)

  // Subtração -
    console.log('25-5=', 25-5)

  // Resto da divisão %
    let remainder
    remainder = 8 % 2
    console.log('O resto da divisão é', remainder)

  // Incremento ++
    let increment
    increment = 0
    // O incremento antes da variável é reconhecido de imediato, o incremento após a variável só será reconhecido na próxima vez que for invocado a variável

    console.log(++increment)
    console.log(increment++)
    console.log(increment)

  //Decremento --
    // O principio de uso do decrement é igual ao do increment

    let decrement
    decrement = 9
    console.log(--decrement)

  // Exponencial **
    console.log(3 ** 2)

    // Ou seja, 3 elevado a segunda potencia
